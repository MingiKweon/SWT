print("HelloWorld") #기본
print("\nHelloWorld") #개행(줄바꿈) (new line)
print("\vHelloWorld") #수직탭(vertical)
print("\tHelloWorld") #수평탭(tab)
print("\rHelloWorld") #캐리지리턴(return) - 옛날 타자기 생각하면 된다. (앞에 적힌 수에 덮어쓰기한다. 글자개수)
print("\fHelloWorld") #폼피드(form feed) - 프린터 관련 이스케이프문자 (프린터에 보내면 연속용지가 다음 페이지의 시작부분으로 넘겨진다)
print("\aHelloWorld") # 벨소리 (난답니다)
print("\bHelloWorld") # 백스페이스? ()
print("\000HelloWorld") # 널문자?
print("\\HelloWorld") # 문자 \ (다른 거 오류나는 거 아니면 그냥 적어도됨)
print("\'HelloWorld\'") # " (다른 거 오류나는 거 아니면 그냥 적어도됨)
print("\"HelloWorld\"") # ' (다른 거 오류나는 거 아니면 그냥 적어도됨)